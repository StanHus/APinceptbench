MATCH_ADDITIONS = '''
## MATCHING-SPECIFIC EVALUATION CRITERIA

### Structure Requirements
- 4-5 terms to match
- Equal or greater number of definitions/descriptions
- Clear, unambiguous matches

### Term Quality
- Terms should be related but distinct concepts
- Avoid terms that could reasonably match multiple definitions
- Terms should be from the same topic/time period

### Definition Quality
- Definitions should uniquely identify their term
- Avoid definitions that are too similar to each other
- Each definition should be complete and self-contained

### Cognitive Demand
- Should require understanding relationships, not just memorization
- Avoid simple vocabulary matching
- Encourage making connections between concepts
'''
